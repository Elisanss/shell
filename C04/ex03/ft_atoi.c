int	ft_atoi(char *str)
{
	int	a;
	int	b;
	int	res;

	a = 0;
	b = 1;
	res = 0;
	while ((str[a] >= '\t' && str[a] <= '\r') || str[a] == ' ')
	{
		a++;
	}
	while (str[a] == '+' || str[a] == '-')
	{
		if (str[a] == '-')
			b *= -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		res = (str[a] - '0') + (res * 10);
		a++;
	}
	return (res * b);
}
